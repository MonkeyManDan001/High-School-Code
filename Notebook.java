public class Notebook implements NoteTaker
{
	public void writingTool()
	{
		System.out.println("Using my notebook to take notes.");
	}
	
	public void beginNotes()
	{
		System.out.println("Opening my notebook to take notes.");
	}
	
	public void saveNotes()
	{
		System.out.println("Putting my notebook in a safe place for saving.");
	}
	
	public void endNotes()
	{
		System.out.println("Closing my notebook.");
	}
}
