public class MicrosoftWord implements NoteTaker
{
	public void writingTool()
	{
		System.out.println("Using Microsoft Word to take notes.");
	}
	
	public void beginNotes()
	{
		System.out.println("Double-clicking on Word to open the application.");
	}
	
	public void saveNotes()
	{
		System.out.println("Clicking \"Save\" in Word to save notes.");
	}
	
	public void endNotes()
	{
		System.out.println("Clicking File and Close File to exit Word.");
	}
}
