//Dan Mine Escape Sequences

#include <iostream>
using namespace std;
int main()
{
	cout << "Symbol\t\tEffect\n\n";
	cout << "\\n\t\tGo to the next line";
	cout << "\n\\\"\t\tDouble-quotation mark";
	cout << "\n\\\'\t\tSingle-quotation mark";
	cout << "\n\\\\\t\tSingle backslash";
	cout << "\n\\t\t\tTab (move five spaces)";

	return 0;
}

/*

Symbol		Effect

\n		Go to the next line
\"		Double-quotation mark
\'		Single-quotation mark
\\		Single backslash
\t		Tab (move five spaces)

 */
