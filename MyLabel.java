public class MyLabel
{
	private String first;
	private String last;
	private String school;
	
	public MyLabel (String inFirst, String inLast, String inSchool)
	{
		first = inFirst;
		last = inLast;
		school = inSchool;
	}
	
	public String getFirst()
	{		
		return first;		
	}
	
	public String getLast()
	{		
		return last;		
	}
	
	public String getSchool()
	{		
		return school;		
	}
}
