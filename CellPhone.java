public class CellPhone implements NoteTaker
{
	public void writingTool()
	{
		System.out.println("Using My CellPhone to take notes.");
	}
	
	public void beginNotes()
	{
		System.out.println("Opening the \"Notes\" section of my cell phone.");
	}
	
	public void saveNotes()
	{
		System.out.println("Pressing \"Save\" on my cell phone to save notes.");
	}
	
	public void endNotes()
	{
		System.out.println("Closing my cell phone.");
	}
}
